//! `wasm4games` â€” a branchless, `no_std`, engine-agnostic *game-pattern foundry*.
//!
//! Game behaviors (input admission, ticks, collision, combat, status, physics, evidence,
//! promotion) are expressed as **bounded byte-class state** transformed by **branchless
//! kernels** that reuse the verified primitives in [`bcinr_logic`]. Every meaningful
//! transition can emit **object-centric evidence** ([`evidence::ocel`]), **runtime span
//! codes** ([`evidence::otel`]), a **receipt chain** ([`evidence::receipt`]), and a
//! **deterministic replay frame** ([`evidence::replay`]).
//!
//! # Quick Start
//!
//! Call a pattern kernel directly for zero-allocation, branch-free game logic:
//!
//! ```
//! use wasm4games::patterns::damage_applied;
//!
//! // state: bits[0..16] = current HP (200)
//! // input: bits[0..16] = base damage (25), bit[16] = crit flag (0 = no crit)
//! let new_state = damage_applied(200, 25);
//! assert_eq!(new_state, 175);
//!
//! // With a crit (bit 16 set), damage is doubled.
//! let crit_state = damage_applied(200, 25 | (1 << 16));
//! assert_eq!(crit_state, 150);
//! ```
//!
//! # Pattern Kernels
//!
//! All 75 pattern kernels live in [`patterns`]. Each kernel is a pure function
//! `(state: u64, input: u64) -> u64` using a packed-u64 ABI. The complete catalog
//! is exposed via [`patterns::PATTERN_REGISTRY`] as [`ir::PatternSpec`] data, which
//! allows iteration, self-checking, and portability verification without any runtime
//! reflection.
//!
//! # Evidence Channels
//!
//! Kernels are intentionally free of side effects; evidence is emitted by the caller
//! using the four channels in [`evidence`]:
//!
//! | Module | Purpose |
//! |---|---|
//! | [`evidence::ocel`] | Object-centric event log (OCEL-style) |
//! | [`evidence::otel`] | Runtime span codes |
//! | [`evidence::receipt`] | Deterministic rolling receipt chain |
//! | [`evidence::replay`] | Deterministic replay frames |
//!
//! # No-std Usage
//!
//! The crate is `#![no_std]` by default. Enable the `alloc` feature for growable
//! structures like [`evidence::ocel::OcelLog`]. Enable the `std` feature if you need
//! the full standard library in tests or integration code.
//!
//! ## Doctrine
//!
//! - Engines project worlds; `wasm4games` operates patterns; `wasm4pm` admits evidence.
//! - Kernels are **generated by ggen** from the input surface in `ggen/`; the committed
//!   files under [`patterns`] are the offline source of truth. See the "GGEN-ONLY USER
//!   SURFACE" covenant in the docs.
//! - `wasm4pm` is *referenced, not vendored*: [`compat`] holds dependency-free mirror
//!   types shaped like `wasm4pm-compat`; the real admission bridge lives in the
//!   workspace-excluded `wasm4games-wasm4pm` crate.

